i18n.on('initialized', () => {
  const locales = Localization.getLocales();
  if (locales.length > 0) {
    const code = locales[0].languageCode;
    const langMap: Record<string, string> = { zh: 'zh-CN', en: 'en', ja: 'ja' };
    const target = langMap[code ?? ''] || 'en';  // Fallback to English for unsupported languages
    if (i18n.language !== target) {
      i18n.changeLanguage(target);
    }
  }
});

